/**
 * A single research focus unit inside a v2 task contract.
 * Each focus has its own hypothesis, mandatory research steps, expected artifacts,
 * and optional statistical tests that the worker must satisfy.
 */
export interface ResearchFocus {
  /** Short machine-readable key, e.g. 'cold_phase', 'hybrid_entry', 'distribution_bias' */
  key: string;
  title: string;
  hypothesis: string;
  required_steps: string[];
  /** Artifact keys expected from this focus (matched against changed_files / result) */
  target_outputs: string[];
  /** Statistical test expressions that must appear in evidence, e.g. 'perm_p < 0.05' */
  statistical_tests?: string[];
}

